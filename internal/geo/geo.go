// Package geo превращает координаты лота в ключ «района» — гео-ячейку.
// Площадки не отдают единый район строкой, зато обе дают координаты,
// поэтому сегментируем рынок по сетке ~1–1.5 км одинаково для всех.
package geo

import (
	"fmt"
	"math"
)

// Размер ячейки в градусах. На широте Минска (~53.9°) 0.02° долготы ≈ 1.3 км,
// 0.015° широты ≈ 1.7 км — компромисс между «районом» и наполненностью выборки.
const (
	cellLat = 0.015
	cellLon = 0.020
)

// Cell возвращает ключ гео-ячейки для координат. Пустая строка, если
// координат нет (0,0) — такой лот в сегментную статистику не попадёт.
func Cell(lat, lon float64) string {
	if lat == 0 && lon == 0 {
		return ""
	}
	la := math.Round(lat/cellLat) * cellLat
	lo := math.Round(lon/cellLon) * cellLon
	return fmt.Sprintf("%.3f:%.3f", la, lo)
}
